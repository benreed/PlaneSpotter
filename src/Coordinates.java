
public class Coordinates {
	private double x, y, z, radius;
	
	public Coordinates() {
		
	}
	
	public Coordinates(double x, double y, double z, double radius) {
		
	}

	public double getRadius() {
		return radius;
	}

	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}

	public double getZ() {
		return z;
	}

	public void setRadius(double radius) {
		this.radius = radius;
	}

	public void setX(double x) {
		this.x = x;
	}

	public void setY(double y) {
		this.y = y;
	}

	public void setZ(double z) {
		this.z = z;
	}
}
